private boolean validarDNI(String dni) {
    // Verificar si el DNI tiene exactamente 8 caracteres numéricos
    return dni.matches("\\d{8}");
}
private void guardarEnArchivo() {
    String datosAGuardar = prepararDatosParaGuardar();
    String nombreArchivo = "datos_usuario.txt"; // Nombre del archivo donde se guardarán los datos
    
    try (BufferedWriter writer = new BufferedWriter(new FileWriter(nombreArchivo))) {
        writer.write(datosAGuardar);
        JOptionPane.showMessageDialog(this, "Los datos se han guardado correctamente en " + nombreArchivo, "Guardado exitoso", JOptionPane.INFORMATION_MESSAGE);
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar los datos: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}

private String prepararDatosParaGuardar() {
    String datos = "";
    datos += "Nombre: " + nombre + "\n";
    datos += "Apellidos: " + apellidos + "\n";
    datos += "Correo: " + correo + "\n";
    datos += "DNI: " + dni + "\n";
    datos += "País: " + país + "\n";
    // Puedes agregar más campos según sea necesario

    return datos;
}

