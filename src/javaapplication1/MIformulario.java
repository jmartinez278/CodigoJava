    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
capturarDatos();

     if (jCheckBox1.isSelected()) {
        
        if (nombre.isEmpty() && apellidos.isEmpty() && correo.isEmpty() && país.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Los campos obligatorios no pueden estar vacíos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        else{
          if (!validarDNI(dni)) {
            JOptionPane.showMessageDialog(this, "El DNI debe contener exactamente 8 dígitos.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si el DNI no es válido
        }
          else {
              if (contraseña.equals(confirmarContraseña)) {
                guardarEnArchivo();
                 }         
              else {
                     JOptionPane.showMessageDialog(null, "Los campos de contraseña deben ser iguales.", "Error", JOptionPane.ERROR_MESSAGE);
                }
               } 
            }
    }
    else {
    JOptionPane.showMessageDialog(null, "Debe de aceptar los términos y condiciones antes de continuar.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton7ActionPerformed

